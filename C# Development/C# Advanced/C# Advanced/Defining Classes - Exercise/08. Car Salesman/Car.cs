namespace Problem_10._Car_Salesman
{
    public class Car
    {
        private string model;
        private string engine;
        private string weight;
        private string color;

        public string Color
        {
            get { return color; }
            set { color = value; }
        }

        public string Weight
        {
            get { return weight; }
            set { weight = value; }
        }

        public string Engine
        {
            get { return engine; }
            set { engine = value; }
        }

        public string Model
        {
            get { return model; }
            set { model = value; }
        }
    }
}
